
package exercicio6;
public class Exercicio6 {
    public static void main(String[] args) {
        System.out.println("Um abaixo do outro:");
        for (int i = 1; i <= 20; i++) {
            System.out.println(i);
        }

        System.out.println("\nUm ao lado do outro:");
        for (int i = 1; i <= 20; i++) {
            System.out.print(i + " ");
        }
    }
}

